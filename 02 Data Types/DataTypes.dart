void main(){
    // Data Types
    // Numbers, Strings, Booleans

    // Strings
    print("Ejercicio 1:");
    String name1 = "Melvin";
    var name2 = 'Ernesto';
    print("My name is: $name1 $name2");

    // Integers
    print("Ejercicio 2:");
    int amount1 = 100;
    var amount2 = 200;
    print("Integer 1 = $amount1 Integer 2 = $amount2");

    // Doubles
    print("Ejercicio 3:");
    double otherNum = 30.19;
    print("Double: $otherNum");

    // Booleans
    print("Ejercicio 3:");
    bool myBool1 = false;
    var myBool2 = true;
    print("Boolean: $myBool1 and $myBool2");

    // Dynamic
    print("Ejercicio 5:");
    dynamic weakVariable = "Melvin Ernesto Santana Garcia";
    print("weakVariable 1: $weakVariable");

    print("Ejercicio 6:");
    weakVariable = 30;
    print("weakVariable 2: $weakVariable");

    // Null
    print("Ejercicio 7:");
    weakVariable = null;
    print(weakVariable);
}