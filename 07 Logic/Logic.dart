main(){
    // Logic - if, if/else, if else if

    // if
    var num = 4;

    if (num == 5) {
        print("The number is 5!");
    }

    // if/else
    if (num == 5) {
        print("The number is 5!");
    } else {
        print("The number is NOT 5!");
    }

    // if else if
    if (num == 5) {
        print("The number is 5!");
    } else if (num == 3) {
        print("The number is 3!");
    } else {
        print("The number is NOT 3 or 5, it is $num");
    }
}